// Raw fs (not the vault adapter): the companion token store and the generated header helper live in
// the plugin's data folder, outside the vault note tree; the CLI reads them per connection.
import { promises as fs } from "fs";
import * as path from "path";
import { warn } from "../util/log";

export interface CompanionConfigPaths {
  // Absolute plugin data folder — the token store + helper live here (NOT in $CLAUDE_CONFIG_DIR/ide/).
  pluginDir: string;
  // Absolute vault/project root — where the user runs `claude`, so `.mcp.json` lives here.
  vaultRoot: string;
}

export interface TokenStore {
  port: number;
  authToken: string;
}

const STORE_NAME = "companion.json";
const HELPER_NAME = "companion-headers.cjs";
const MCP_SERVER_KEY = "obsidian-vault";

// The headers helper: a standalone CommonJS script (.cjs so a nearby "type":"module" can't break it)
// that prints the Authorization header from the token store sitting next to it. Claude Code runs it
// per connection, so a restart with a new token keeps working without rewriting the header.
const HELPER_SOURCE = `// Auto-generated by Code Workbench. Prints the MCP Authorization header from the local token store.
const fs = require("fs");
const path = require("path");
try {
  const store = JSON.parse(fs.readFileSync(path.join(__dirname, ${JSON.stringify(STORE_NAME)}), "utf8"));
  process.stdout.write(JSON.stringify({ Authorization: "Bearer " + store.authToken }));
} catch (e) {
  process.exit(1);
}
`;

// Token store, auto-config (.mcp.json), the headers helper, and git-exclude for the companion server.
// All writes go through the Obsidian/host filesystem with restrictive perms; the token is never logged.
export class CompanionConfig {
  constructor(private readonly paths: CompanionConfigPaths) {}

  tokenStorePath(): string {
    return path.join(this.paths.pluginDir, STORE_NAME);
  }

  helperPath(): string {
    return path.join(this.paths.pluginDir, HELPER_NAME);
  }

  mcpJsonPath(): string {
    return path.join(this.paths.vaultRoot, ".mcp.json");
  }

  // Atomically write { port, authToken } with 0600 (dir 0700). Replaces any previous store.
  async writeTokenStore(store: TokenStore): Promise<void> {
    const dir = this.paths.pluginDir;
    await fs.mkdir(dir, { recursive: true });
    await this.chmodBestEffort(dir, 0o700);
    const target = this.tokenStorePath();
    const tmp = `${target}.tmp`;
    await fs.writeFile(tmp, JSON.stringify(store), { mode: 0o600 });
    await this.chmodBestEffort(tmp, 0o600);
    await fs.rename(tmp, target);
  }

  async readTokenStore(): Promise<TokenStore | null> {
    try {
      const data = JSON.parse(await fs.readFile(this.tokenStorePath(), "utf8")) as Partial<TokenStore>;
      if (typeof data.port === "number" && typeof data.authToken === "string") {
        return { port: data.port, authToken: data.authToken };
      }
    } catch {
      // absent or unreadable
    }
    return null;
  }

  async clearTokenStore(): Promise<void> {
    await fs.unlink(this.tokenStorePath()).catch(() => undefined);
  }

  async writeHelper(): Promise<void> {
    const target = this.helperPath();
    await fs.writeFile(target, HELPER_SOURCE, { mode: 0o700 });
    await this.chmodBestEffort(target, 0o700);
  }

  // Merge our server entry into .mcp.json, preserving any other servers. Backs the original up once
  // (.mcp.json.bak) so an existing user file is never clobbered. Rewriting on each start refreshes the
  // port (FR-025: reconnect after a port change).
  async writeMcpJson(port: number): Promise<void> {
    const target = this.mcpJsonPath();
    let config: Record<string, unknown> = {};
    let existing: string | null = null;
    try {
      existing = await fs.readFile(target, "utf8");
    } catch {
      existing = null;
    }
    if (existing !== null) {
      // Back up the user's original exactly once, before our first edit.
      const backup = `${target}.bak`;
      if (!(await this.exists(backup))) {
        await fs.writeFile(backup, existing).catch((e) => warn("mcp backup failed", e));
      }
      try {
        const parsed: unknown = JSON.parse(existing);
        if (parsed && typeof parsed === "object") config = parsed as Record<string, unknown>;
      } catch {
        // unparseable foreign file — already backed up; start fresh
        config = {};
      }
    }
    const servers =
      config.mcpServers && typeof config.mcpServers === "object"
        ? (config.mcpServers as Record<string, unknown>)
        : {};
    servers[MCP_SERVER_KEY] = {
      type: "http",
      url: `http://127.0.0.1:${port}/mcp`,
      headersHelper: `node "${this.helperPath()}"`,
    };
    config.mcpServers = servers;
    await fs.writeFile(target, `${JSON.stringify(config, null, 2)}\n`);
  }

  // Remove only our entry, leaving any other servers (and the file) intact.
  async removeMcpEntry(): Promise<void> {
    const target = this.mcpJsonPath();
    let parsed: Record<string, unknown>;
    try {
      parsed = JSON.parse(await fs.readFile(target, "utf8")) as Record<string, unknown>;
    } catch {
      return;
    }
    const servers = parsed.mcpServers as Record<string, unknown> | undefined;
    if (!servers || !(MCP_SERVER_KEY in servers)) return;
    delete servers[MCP_SERVER_KEY];
    await fs.writeFile(target, `${JSON.stringify(parsed, null, 2)}\n`).catch((e) => warn("mcp cleanup failed", e));
  }

  // If the vault is a git repo, keep .mcp.json + helper + token store out of version control via
  // .git/info/exclude (project rule: private artifacts via exclude, not a public .gitignore).
  async addGitExclude(): Promise<void> {
    const excludePath = await this.gitExcludePath();
    if (!excludePath) return;
    const lines = [".mcp.json", ".mcp.json.bak"];
    for (const abs of [this.tokenStorePath(), this.helperPath()]) {
      const rel = path.relative(this.paths.vaultRoot, abs);
      if (!rel.startsWith("..") && !path.isAbsolute(rel)) lines.push(rel.split(path.sep).join("/"));
    }
    let current = "";
    try {
      current = await fs.readFile(excludePath, "utf8");
    } catch {
      current = "";
    }
    const have = new Set(current.split(/\r?\n/).map((l) => l.trim()));
    const missing = lines.filter((l) => !have.has(l));
    if (missing.length === 0) return;
    const prefix = current.length > 0 && !current.endsWith("\n") ? "\n" : "";
    await fs
      .appendFile(excludePath, `${prefix}${missing.join("\n")}\n`)
      .catch((e) => warn("git exclude update failed", e));
  }

  // Resolve .git/info/exclude, handling both a .git directory and a .git file (worktree/submodule).
  private async gitExcludePath(): Promise<string | null> {
    const dotGit = path.join(this.paths.vaultRoot, ".git");
    let stat;
    try {
      stat = await fs.stat(dotGit);
    } catch {
      return null;
    }
    let gitDir = dotGit;
    if (stat.isFile()) {
      try {
        const content = await fs.readFile(dotGit, "utf8");
        const match = content.match(/gitdir:\s*(.+)\s*/);
        if (!match) return null;
        gitDir = path.resolve(this.paths.vaultRoot, match[1].trim());
      } catch {
        return null;
      }
    } else if (!stat.isDirectory()) {
      return null;
    }
    const infoDir = path.join(gitDir, "info");
    await fs.mkdir(infoDir, { recursive: true }).catch(() => undefined);
    return path.join(infoDir, "exclude");
  }

  private async exists(p: string): Promise<boolean> {
    try {
      await fs.access(p);
      return true;
    } catch {
      return false;
    }
  }

  private async chmodBestEffort(p: string, mode: number): Promise<void> {
    if (process.platform === "win32") return;
    try {
      await fs.chmod(p, mode);
    } catch {
      // permissions are best-effort on exotic filesystems
    }
  }
}
