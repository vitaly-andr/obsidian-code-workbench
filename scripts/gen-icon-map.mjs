// Generates src/icon-map.ts: the file/folder -> icon-name mapping distilled from material-icon-theme
// (Philipp Kief, MIT). Only the lookup tables are bundled (~200KB); the SVGs themselves are fetched
// on demand from jsDelivr and cached. The "expanded folder" icon is derived as <name>-open, so the
// folderNamesExpanded table (~148KB, 100% derivable) is dropped.
//
// The theme config is fetched from jsDelivr (same CDN we use at runtime) so the package isn't a
// dependency — keeps node_modules lean. Bump VERSION to update the icon set. Run:
//   node scripts/gen-icon-map.mjs
import { writeFileSync } from "node:fs";

const VERSION = "5.35.0";
const url = `https://cdn.jsdelivr.net/npm/material-icon-theme@${VERSION}/dist/material-icons.json`;

const res = await fetch(url);
if (!res.ok) throw new Error(`fetch ${url} -> ${res.status}`);
const m = await res.json();

// Sanity-check the assumption that lets us drop folderNamesExpanded.
for (const [k, v] of Object.entries(m.folderNames || {})) {
  if (m.folderNamesExpanded?.[k] !== `${v}-open`) {
    throw new Error(`folderNamesExpanded not derivable for "${k}" (${m.folderNamesExpanded?.[k]} != ${v}-open)`);
  }
}

const byExt = m.fileExtensions || {};
const byName = m.fileNames || {};
const byFolder = m.folderNames || {};
// Obsidian flags any hardcoded ".obsidian" — the config dir is user-renamable (Vault#configDir).
// Drop the upstream ".obsidian" folder entry; the config-folder icon is assigned via configDir in
// src/icons/icon-names.ts instead.
delete byFolder[".obsidian"];

const out =
  `// SPDX-License-Identifier: LicenseRef-PolyForm-Shield-1.0.0\n` +
  `// Copyright 2026 Vitaly Andrianov. See LICENSE.\n\n` +
  `// File/folder -> Material icon-name lookup, distilled from material-icon-theme ${VERSION}\n` +
  `// (Philipp Kief, MIT — see THIRD-PARTY-LICENSES). Generated by scripts/gen-icon-map.mjs — do not\n` +
  `// edit by hand. SVGs are fetched on demand from jsDelivr; the expanded-folder icon is <name>-open.\n` +
  `export const ICONS_VERSION = ${JSON.stringify(VERSION)};\n` +
  `export const ICON_CDN = "https://cdn.jsdelivr.net/npm/material-icon-theme@" + ICONS_VERSION + "/icons";\n` +
  `export const DEFAULT_FILE = ${JSON.stringify(m.file)};\n` +
  `export const DEFAULT_FOLDER = ${JSON.stringify(m.folder)};\n` +
  `export const BY_EXT: Record<string, string> = ${JSON.stringify(byExt)};\n` +
  `export const BY_NAME: Record<string, string> = ${JSON.stringify(byName)};\n` +
  `export const BY_FOLDER: Record<string, string> = ${JSON.stringify(byFolder)};\n`;

writeFileSync("src/icon-map.ts", out);
const kb = (n) => (n / 1024).toFixed(1) + " KB";
console.log(`material-icon-theme ${VERSION} (fetched from jsDelivr)`);
console.log(`byExt=${Object.keys(byExt).length} byName=${Object.keys(byName).length} byFolder=${Object.keys(byFolder).length}`);
console.log(`wrote src/icon-map.ts: ${kb(out.length)}`);
